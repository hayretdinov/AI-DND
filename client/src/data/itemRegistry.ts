export type ItemId = keyof typeof itemRegistry;

export const MVP_ITEM_IDS = Object.keys(itemRegistry) as ItemId[];

export function isItemId(value: string): value is ItemId {
  return value in itemRegistry;
}

export function getItemTemplateById(itemId: string) {
  return isItemId(itemId) ? itemRegistry[itemId] : null;
}

export function validateItemAssetPaths() {
  for (const item of Object.values(itemRegistry)) {
    if (!item.iconUrl || !knownItemIconUrls.has(item.iconUrl)) {
      console.warn("[ItemAssets] Missing icon", {
        itemId: item.itemId,
        iconUrl: item.iconUrl ?? null,
        expectedIconUrl: expectedMissingIconUrls[item.itemId] ?? null,
        fallbackIconType: item.fallbackIconType,
      });
    }
  }
}
