import type {
  EquipmentSlot,
  FallbackIconType,
  InventoryItem,
  InventoryItemQuality,
  InventoryRarity,
  ItemEffectType,
} from "../types/inventory";
import type { AttackAttribute, DamageType, WeaponType } from "../types/combat";

export type ItemCategory =
  | "consumable"
  | "medicine"
  | "quest"
  | "document"
  | "misc"
  | "tool"
  | "material"
  | "clothing"
  | "weapon"
  | "shield"
  | "accessory";

export type ItemTemplate = Omit<
  InventoryItem,
  "id" | "instanceId" | "quantity" | "condition" | "quality" | "origin" | "owner" | "createdAt"
> & {
  itemId: string;
  category: ItemCategory;
  rarity: Extract<InventoryRarity, "common" | "uncommon" | "rare" | "unique">;
  stackable: boolean;
  maxStack?: number;
  canUse: boolean;
  canEquip: boolean;
  equipmentSlot?: EquipmentSlot;
  weaponType?: WeaponType;
  damageDice?: string;
  damageType?: DamageType;
  attackAttribute?: AttackAttribute;
  armorValue?: number;
  outfitStageOnEquip?: "rags" | "clothes" | "armor";
  effectType?: ItemEffectType;
  effectValue?: number;
  isQuestItem?: boolean;
  craftingMaterial?: boolean;
  iconUrl?: string;
  fallbackIconType: FallbackIconType;
  promptNameRu: string;
  promptNameEn: string;
  whoCanGive: readonly string[];
  defaultQuality?: InventoryItemQuality;
};

function createTemplate(template: Omit<ItemTemplate, "templateId" | "equippable" | "icon" | "slot" | "questItem">): ItemTemplate {
  const legacySlotByEquipmentSlot: Partial<Record<EquipmentSlot, EquipmentSlot>> = {
    body: "chest",
    torso: "chest",
    primaryWeapon: "mainHand",
    secondaryWeapon: "offHand",
    shield: "offHand",
    leftRing: "ring1",
    rightRing: "ring2",
  };

  return {
    ...template,
    templateId: template.itemId,
    equippable: template.canEquip,
    slot: template.equipmentSlot ? legacySlotByEquipmentSlot[template.equipmentSlot] ?? template.equipmentSlot : undefined,
    questItem: template.isQuestItem,
    icon: template.iconUrl ?? template.fallbackIconType,
  };
}

